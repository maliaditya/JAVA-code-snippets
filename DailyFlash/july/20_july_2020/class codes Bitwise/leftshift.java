class LeftShift{

    public static void main(String[] args){

        int num = 75;
        int num1 = 2;
        int num2 = -12;
        int ans;
        ans = num << 4;
        System.out.println(ans);
        ans = num1 << 2;
        System.out.println(ans);
        ans = num2 << 2;

        System.out.println(ans);
    }
}